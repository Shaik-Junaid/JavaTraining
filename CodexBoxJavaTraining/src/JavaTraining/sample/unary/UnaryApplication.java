package JavaTraining.sample.unary;

public class UnaryApplication {
    // Take any One Number and Convert that number into Unary Operator

    // Main Method

    public static void main(String[] args) {

        // Assign any one integer value
        int num1 = 67;

        // Print the number Before the unary operator

        System.out.println("Before applying the unary Operator: " + num1);

        // Apply Unary operator

        num1 = -num1;

        // Print the number After the unary operator

        System.out.println("After applying the unary operator: " + num1);
    }
}
